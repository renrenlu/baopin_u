function likesToNumber(value: string) {
  const normalized = value.replaceAll(",", "").trim();
  const match = normalized.match(/([\d.]+)\s*万/);
  if (match) return Number(match[1]) * 10_000;
  const number = Number.parseFloat(normalized);
  return Number.isFinite(number) ? number : 0;
}

function likeGap(works: HookWork[]) {
  const high = works.find((work) => work.level === "高赞");
  const low = works.find((work) => work.level !== "高赞");
  if (!high || !low) return undefined;
  const highLikes = likesToNumber(high.likes);
  const lowLikes = likesToNumber(low.likes);
  if (!highLikes || !lowLikes || highLikes <= lowLikes) return undefined;
  const ratio = highLikes / lowLikes;
  return ratio >= 100
    ? `${Math.round(ratio)} 倍`
    : `${ratio.toFixed(ratio >= 10 ? 0 : 1)} 倍`;
}

export function getHookInsight(date: string, questionId: number, works: HookWork[]): HookInsight {
  const saved = INSIGHTS[`${date}-${questionId}`];
  const high = works.find((work) => work.level === "高赞");
  const low = works.find((work) => work.level !== "高赞");
  return {
    highSignal: saved?.highSignal
      ?? `高赞标题先给出更明确的传播承诺：“${high?.title ?? "—"}”。`,
    lowSignal: saved?.lowSignal
      ?? `低赞标题“${low?.title ?? "—"}”需要进一步收窄对象、场景或结果。`,
    takeaway: saved?.takeaway
      ?? "复写时保留具体对象、冲突和结果，删掉不能帮助读者做判断的宽泛词。",
    likeGap: likeGap(works),
  };
}
